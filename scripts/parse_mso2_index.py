# ── Skip lines that are page headers/noise ────────────────────────────────────
NOISE = re.compile(
    r'^(Index-\d+|2 Series MSO|Index\s*$|Command Groups)',
    re.IGNORECASE
)

def parse_headers(raw: str) -> list[str]:
    headers = []
    pending = ""
    for line in raw.splitlines():
        line = line.strip()
        if not line or NOISE.match(line):
            pending = ""
            continue
        # If line ends with ':', it continues on the next line (PDF word-wrap)
        if line.endswith(':'):
            pending += line
            continue
        # Append to any pending prefix
        full = pending + line
        pending = ""
        # Strip page reference:  ", 2-NNN" or " , 2-NNN"
        full = re.sub(r'\s*,\s*\d+-\d+\s*$', '', full).strip()
        if full:
            headers.append(full)
    return headers

