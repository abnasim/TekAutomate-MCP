import * as path from 'path';
import { getToolRegistry, resetToolRegistry, type MicroTool } from '../src/core/toolRegistry';
import { getToolSearchEngine, resetToolSearchEngine } from '../src/core/toolSearch';
import { tekRouter, type RouterRequest } from '../src/core/toolRouter';
import { hydrateBuiltinShortcuts } from '../src/core/toolHydrator';
import { loadProviderManifests } from '../src/core/providerLoader';
import { bootRouter, getRouterHealth } from '../src/core/routerIntegration';
